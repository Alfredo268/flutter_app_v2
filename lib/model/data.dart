class Data {
  String code;
  String abbr;
  String description;
  String name;
  String unit;
  String value;
  int year;

  Data(code, abbr, description, name, unit, value, year){
    this.code = code;
    this.abbr = abbr;
    this.description = description;
    this.name = name;
    this.unit = unit;
    this.value = value;
    this.year = year;
  }
}